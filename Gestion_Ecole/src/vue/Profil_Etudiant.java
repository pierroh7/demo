    public Profil_Etudiant(String id, String name, String surname, String classe) {
        initComponents();
        String etudiant = name + " " + surname + " " + classe + " | " + id;
        Etudiant.setText(etudiant);
        
        tab_notes.addColumn("DS1");
        tab_notes.addColumn("DS2");
        tab_notes.addColumn("NS1");
        tab_notes.addColumn("NS2");
        
        
        //Communication et affichage de la base de données
        try{
            etat=(Statement) connect.getConnection().createStatement();
            result = etat.executeQuery("SELECT * from notes_math WHERE ID="+id);
            while(result.next()){
            tab_notes.addRow(new Object[]{result.getString("DS1"), result.getString("DS2"),
                result.getString("NS1"),result.getString("NS2")});
            }
            
            result = etat.executeQuery("SELECT * from notes_elec WHERE ID="+id);
            while(result.next()){
            tab_notes.addRow(new Object[]{result.getString("DS1"), result.getString("DS2"),
                result.getString("NS1"),result.getString("NS2")});
            }
            
        }catch(Exception e){
            System.out.println(e);
        }
        
        notes.setModel(tab_notes);
    }

