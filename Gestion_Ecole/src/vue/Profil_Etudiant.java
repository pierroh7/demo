    private void deplace(int i){
        try{
            txt_ds1.setText(tab_notes.getValueAt(i,0).toString());
            txt_ds2.setText(tab_notes.getValueAt(i,1).toString());
            txt_ns1.setText(tab_notes.getValueAt(i,2).toString());
            txt_ns2.setText(tab_notes.getValueAt(i,3).toString());
        }catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "erreur de deplacment "+e.getLocalizedMessage());
        }
    }
    
